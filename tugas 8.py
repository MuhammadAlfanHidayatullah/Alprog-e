              #aritmatika
x = 24
y = 90

#penjumlahan
hasil = x + y
print ("hasil dari x + y = ", hasil)

#pengurangan
hasil = x - y
print ("nilai dari x - y = ", hasil)

#pembagian
hasil = x / y
print ("hasil dari x / y = ", hasil)

#perkalian
hasil = x * y
print ("hasil dari x * y =", hasil)

#exponen (perpangkatan)
hasil = x ** y
print ("nilai dari x eksponen y = ", hasil)

#Modulus (sisa bagi)
hasil = x % y
print ("hasil dari x mod y = ", hasil)

#floor division
hasil = x // y
print ("hasil dari x FD y = ", hasil)
